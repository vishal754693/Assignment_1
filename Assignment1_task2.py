# Create a Personalized Greeting (with user input)

# Step 1: Take user's first name and last name as input
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")

# Step 2: Concatenate first name and last name into a full name
full_name = first_name + " " + last_name

# Step 3: Print a personalized greeting message
print(f"Hello, {full_name}! Welcome!")
